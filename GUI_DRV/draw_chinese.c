void draw_chinese(uint16_t left_top_x, uint16_t left_top_y, uint8_t chinese_type, uint16_t unicode, uint16_t color)
{
	uint16_t font_array_row, font_array_column, font_actual_row, font_actual_column;
	uint16_t i, j, m;
	uint8_t temp;
	uint8_t buffer[128] =
    {
        0x00, 0x00, 0x60, 0x60, 0x60, 0x60, 0x60, 0xff, 0xff, 0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0xff, 0xff, 0x60, 
        0x60, 0x60, 0x60, 0x60, 0x00, 0x00, 0x00, 0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0x7f, 0x7f, 0x60, 0x60, 0x60,
        0x60, 0x60, 0x60, 0x7f, 0x7f, 0x60, 0x60, 0x60, 0x60, 0x60, 0x60, 0x00, 0x00, 0x40, 0xe0, 0x70, 0x38, 0x1c,
        0x0e, 0x07, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02, 0x07, 0x0e, 0x1c, 0x38, 0x70, 0xe0, 0x40, 0x00
    };
	
	font_array_row = 3;
	font_array_column = 24;
	font_actual_row = 24;
	font_actual_column = 24;
	
	for(i = 0; i < font_array_row; i++)
	{
		for(j = 0; j < font_array_column; j++)
		{
			temp = buffer[font_array_column * i + j];
			
			for(m = 0; m < 8; m++)
			{
				if(temp & 0x01)
				{
					hardware.hal_lcd.lcd_draw_pixel(left_top_x + j,  left_top_y + 8 * i + m, color);
				}
                
                temp = temp >> 1;
			}
		}
	}
}

